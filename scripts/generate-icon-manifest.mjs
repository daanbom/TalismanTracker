/**
 * Scans public/icons/ for PNG files and writes src/data/availableIcons.js
 * containing only the icon entries that have a corresponding PNG.
 *
 * Run after adding new PNG files: node scripts/generate-icon-manifest.mjs
 */

import { readdirSync, writeFileSync } from 'fs'
import path from 'path'
import { fileURLToPath } from 'url'
import { ALL_ICONS } from '../src/data/allIcons.js'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const iconsDir = path.join(__dirname, '..', 'public', 'icons')
const outFile = path.join(__dirname, '..', 'src', 'data', 'availableIcons.js')

const pngKeys = new Set(
  readdirSync(iconsDir)
    .filter(f => f.endsWith('.png'))
    .map(f => f.replace('.png', ''))
)

const available = ALL_ICONS.filter(i => pngKeys.has(i.key))

const lines = available.map(
  i => `  { key: '${i.key}', name: '${i.name}', expansion: '${i.expansion}' },`
)

const output = `// Auto-generated by scripts/generate-icon-manifest.mjs — do not edit manually
export const AVAILABLE_ICONS = [
${lines.join('\n')}
]
`

writeFileSync(outFile, output)
console.log(`availableIcons.js updated — ${available.length} icon(s): ${available.map(i => i.key).join(', ')}`)
